package adbconnect;

import java.io.File;

import org.eclipse.core.runtime.preferences.InstanceScope;
import org.eclipse.jface.preference.IPreferenceStore;
import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.eclipse.ui.preferences.ScopedPreferenceStore;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "AdbConnect";

	public static final String IP_ADDRESS_SETTING = "IP_ADDRESS";
	public static final String PORT_NUMBER_SETTING = "PORT_NUMBER";

	// The shared instance
	private static Activator plugin;

	/**
	 * The constructor
	 */
	public Activator() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	@Override
    public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	@Override
    public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

	/**
	 * Returns an image descriptor for the image file at the given
	 * plug-in relative path
	 *
	 * @param path the path
	 * @return the image descriptor
	 */
	public static ImageDescriptor getImageDescriptor(String path) {
		return imageDescriptorFromPlugin(PLUGIN_ID, path);
	}

	public static String getDeviceIpAddress() {
	    return plugin.getPreferenceStore().getString(IP_ADDRESS_SETTING);
	}

	public static String getDevicePortNumber() {
	    return plugin.getPreferenceStore().getString(PORT_NUMBER_SETTING);
	}

	public static String getPathToAdb() {
	    IPreferenceStore ps = new ScopedPreferenceStore(InstanceScope.INSTANCE,
	            "com.android.ide.eclipse.adt");
	    return ps.getString("com.android.ide.eclipse.adt.sdk") + File.separator + "platform-tools" + File.separator;
	}
}
